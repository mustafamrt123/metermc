// Copy Server IP Function
function copyServerIp() {
    const serverIp = document.getElementById('serverIp').textContent;
    
    // Modern clipboard API
    if (navigator.clipboard && navigator.clipboard.writeText) {
        navigator.clipboard.writeText(serverIp).then(() => {
            showNotification('Sunucu adresi kopyalandı: ' + serverIp);
        }).catch(err => {
            console.error('Kopyalama hatası:', err);
            fallbackCopy(serverIp);
        });
    } else {
        // Fallback for older browsers
        fallbackCopy(serverIp);
    }
}

function fallbackCopy(text) {
    const textArea = document.createElement('textarea');
    textArea.value = text;
    textArea.style.position = 'fixed';
    textArea.style.left = '-999999px';
    textArea.style.top = '-999999px';
    document.body.appendChild(textArea);
    textArea.focus();
    textArea.select();
    
    try {
        document.execCommand('copy');
        showNotification('Sunucu adresi kopyalandı: ' + text);
    } catch (err) {
        console.error('Kopyalama hatası:', err);
        showNotification('Kopyalama başarısız oldu. Lütfen manuel olarak kopyalayın.', 'error');
    }
    
    document.body.removeChild(textArea);
}

// Notification System
function showNotification(message, type = 'success') {
    // Remove existing notification if any
    const existingNotification = document.querySelector('.notification');
    if (existingNotification) {
        existingNotification.remove();
    }

    const notification = document.createElement('div');
    notification.className = `notification notification-${type}`;
    notification.textContent = message;
    
    // Styles
    notification.style.cssText = `
        position: fixed;
        top: 20px;
        right: 20px;
        background: ${type === 'error' ? '#ff6b6b' : '#4a90e2'};
        color: white;
        padding: 15px 25px;
        border-radius: 10px;
        box-shadow: 0 10px 30px rgba(0, 0, 0, 0.3);
        z-index: 10000;
        animation: slideInRight 0.3s ease;
        max-width: 400px;
        font-weight: 500;
    `;
    
    document.body.appendChild(notification);
    
    setTimeout(() => {
        notification.style.animation = 'slideOutRight 0.3s ease';
        setTimeout(() => {
            notification.remove();
        }, 300);
    }, 3000);
}

